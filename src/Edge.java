package gdbb;

public class Edge {
    private String id;
    private String src;
    private String dst;

    public Edge(String id, String src, String dst) {
        this.id = id;
        this.src = src;
        this.dst = dst;
    }

    public String getDst() {
        return dst;
    }

    public void setDst(String dst) {
        this.dst = dst;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getSrc() {
        return src;
    }

    public void setSrc(String src) {
        this.src = src;
    }

}